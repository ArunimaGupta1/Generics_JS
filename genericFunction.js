function display(items) {
    for (var i = 0; i < items.length; i++) {
        console.log(items[i]);
    }
}
var str = ["Apple", "Ball", "Cat", "Dog", "Egg"];
var num = [23, 34, 5, 67, 89, 90, 23];
console.log("display String Array");
display(str);
console.log("display Numbers Array");
display(num);
